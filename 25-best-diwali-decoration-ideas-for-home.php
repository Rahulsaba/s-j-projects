                            <h6>
                                Diwali Decoration Ideas For Home: The Pooja Room
                            </h6>
                            <p>
                                Earthen Pots And Diya Lights: Diyas or earthen/mud lights are quintessentially Diwali. These earthen lights illuminate the magnificence of your home. You can even make your own diyas using flour or soil or transform a basic diya into a beautiful piece with paints, sparkles, and pearls. Apart from the Pooja room or Thali, decorate the staircase, hallways, front door and balcony with dimly lit diyas.
                            </p>
                            <p>

                                Scented Candles: Scented candles are a great addition to Pooja rooms too. Line up candles around the deity and let your home environment soak in the aromas this Diwali.
                            </p>
                            <p>

                                Rangoli Designs: Rangoli is an age-old custom to deck up a home with myriad hues. They look good at the base of the idol in the Pooja house or in the yard.
                            </p>
                            <p>

                                Ganesh And Lakshmi: Diwali is synonymous with worshipping Goddess Lakshmi and Lord Ganesha for health, wealth and happiness. Idols in metal, brass or clay are options you can choose from to place in the Pooja room or Pooja thali. Adorn them with flower buds and bring in fresh energy this year!
                            </p>
                            <p>
                                Floral Adornments: Flowers are adornments you can’t ignore. Get creative this year and place them in a jar or hang them at the entrance or windows in chains to enhance your living space!
                            </p>

